  Future<void> _runRealAnalysis() async {
    // 1. Parse CSV & Calculate Real Bias
    String csvSummary = "No data parsed.";
    Map<String, dynamic>? results;
    
    if (widget.fileBytes != null) {
      try {
        final csvString = utf8.decode(widget.fileBytes!);
        final rows = const CsvToListConverter().convert(csvString);
        results = BiasCalculator.calculate(rows);
        
        if (rows.isNotEmpty) {
          final header = rows.first;
          csvSummary = "Dataset has ${rows.length - 1} rows. Columns: ${header.join(', ')}.\n";
        }
      } catch (e) {
        csvSummary = "Failed to parse CSV: $e\n";
      }
    }

    // Safely apply calculation results to variables
    if (results == null) {
      results = BiasCalculator.calculate([]); // Use fallback dummy
    }
    
    setState(() => _scanStatus = 'Generating AI Insights...');
    
    // Dynamically override local component data with calculable real data
    _fairnessScore = (results['fairnessScore'] as num).toDouble();
    Map<String, double> approvals = (results['groupApprovals'] as Map).cast<String, double>();
    
    // Normalize maxGap back to 0.0-1.0 fraction if it's sent as a 50% integer
    double maxGap = (results['maxGap'] as num).toDouble();
    if (maxGap > 1.0) maxGap = maxGap / 100.0;

    // Map dynamic groups to colors sequentially
    final List<Color> palette = [
      AppColors.primaryBlue,
      AppColors.accentSaffron,
      AppColors.primaryBlueLight,
      const Color(0xFF7C3AED),
      AppColors.error,
    ];
    
    _groups = [];
    int cIdx = 0;
    approvals.forEach((key, rate) {
        // Safe check to guarantee rates are 0.0 to 1.0 for the fl_chart to render correctly
        double normalizedRate = rate > 1.0 ? rate / 100.0 : rate;
        _groups.add(BiasGroup(
          label: key, 
          approvalRate: normalizedRate, 
          color: palette[cIdx % palette.length]
        ));
        cIdx++;
    });

    if (_groups.isEmpty) {
        _groups = [BiasGroup(label: "No Data", approvalRate: 0, color: Colors.grey)];
    }

    // Find the min and max group strings for Gemini prompt accuracy
    String highestGroup = 'Urban Male';
    String lowestGroup = 'SC/ST';
    
    // 2. Ask Gemini for insight
    _geminiInsight = await _fetchGeminiWithRetry(csvSummary, maxGap, _groups);

    // 3. Complete Scan
    if (mounted) {
      setState(() {
        _isScanning = false;
        _scoreAnimation = Tween<double>(begin: 0, end: _fairnessScore / 100).animate(
          CurvedAnimation(parent: _scoreAnimController, curve: Curves.easeOutCubic),
        );
      });
      _scoreAnimController.forward();
    }
  }

  // ─── Gemini AI Fetch with Exponential Backoff Retry ────────────────────────
  Future<String> _fetchGeminiWithRetry(String csvSummary, double maxGap, List<BiasGroup> groups, {int maxRetries = 3}) async {
    // Determine the highest and lowest for exact dynamic prompt context
    final sortedGroups = groups.toList()..sort((a, b) => b.approvalRate.compareTo(a.approvalRate));
    final highest = sortedGroups.first;
    final lowest = sortedGroups.last;
    final gapPercentage = (maxGap * 100).toStringAsFixed(0);

    final prompt = '''
Write a short professional insight in simple English for the ${widget.schemeName} scheme. 
Mention the biggest bias clearly (example: ${lowest.label.replaceAll('\n',' ')} have $gapPercentage% lower approval than ${highest.label.replaceAll('\n',' ')}). 
Explain the impact on ${lowest.label.replaceAll('\n',' ')} applicants. 
End with one practical fix suggestion. Keep it 5-6 lines max.
''';

    if (AppConfig.geminiApiKey == 'YOUR_GEMINI_API_KEY_HERE') {
      return '⚠️ Gemini API Key not set. Fallback:\n\n'
          'The analysis of the ${widget.schemeName} scheme reveals that ${lowest.label.replaceAll('\n',' ')} applicants have a $gapPercentage% lower approval rate compared to ${highest.label.replaceAll('\n',' ')}. This significant disparity suggests potential biases in evaluation processes, hindering equitable inclusion. Addressing this requires a review of eligibility criteria to ensure fair access for all beneficiaries.';
    }

    final model = GenerativeModel(
      model: 'gemini-2.5-flash',
      apiKey: AppConfig.geminiApiKey,
    );
    
    int delaySeconds = 5;
    for (int attempt = 1; attempt <= maxRetries; attempt++) {
      try {
        final response = await model.generateContent([Content.text(prompt)]);
        return response.text?.trim() ?? "Analysis completed, but no insight was returned.";
      } catch (e) {
        final errorStr = e.toString();
        // Catch server busy / 503 errors and retry
        if (errorStr.contains('503') || errorStr.contains('UNAVAILABLE')) {
          if (attempt == maxRetries) {
            return "Gemini is busy right now due to high demand. Please tap 'Scan for Bias' again in 30 seconds.";
          }
          // Visually inform the user that it's retrying
          if (mounted) {
            setState(() => _scanStatus = 'High demand. Retrying in ${delaySeconds}s (Attempt $attempt/$maxRetries)...');
          }
          await Future.delayed(Duration(seconds: delaySeconds));
          delaySeconds += 5; // Exponential-ish backoff: 5s, 10s, 15s
        } else {
          // If it's a different error (like network down), fail immediately
          return "Error connecting to AI Gemini: $errorStr";
        }
      }
    }
    return "Failed to fetch response after $maxRetries attempts. Please try again later.";
  }

