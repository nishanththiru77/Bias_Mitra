    return AppColors.error;
  }



  // ─── What-If adjusted approval rate for lowest group ─────────────────────
  double get _simulatedLowestGroupRate {
    if (_groups.isEmpty) return 0.0;
    final lowest = _groups.reduce((a, b) => a.approvalRate < b.approvalRate ? a : b);
    final highest = _groups.reduce((a, b) => a.approvalRate > b.approvalRate ? a : b);
    final base = lowest.approvalRate;
    final gap = highest.approvalRate - base;
    // Slider closes the gap proportionally
    return base + (gap * _whatIfValue / 100);
  }

